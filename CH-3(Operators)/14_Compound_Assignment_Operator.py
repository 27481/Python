# Compound Assignment Operator 

""" 
A compound assignment operator is a shorthand way of combining an arithmetic or bitwise operation with an assignment operation in a single statement. In other words, it allows you to perform an operation on a variable and then assign the result back to the same variable in one step.
 """
a=5;
a+=4; #! a=a+4;      
a*=6; #! a=a*6;
a&=5; #! a=a&5;
x = 5
x += 3   # equivalent to x = x + 3
print(x) # outputs 8

print(a);

""" 
Compound assignment operators can make your code more concise and easier to read by reducing the amount of repeated code. They can also make your code faster and more efficient, since they avoid the need to create a new variable for the result of the operation.
 """