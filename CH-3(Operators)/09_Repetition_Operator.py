#! IN Python,
#! Repetition operator is the asterisk symbol (*), and it is used to repeat a sequence of characters a certain number of times.

#! The repetition operator can be used with different types of sequences in Python, including (strings, lists, and tuples). When used with a sequence, the repetition operator takes two operands: the sequence to be repeated and the number of times to repeat it.


# Example ======>>>>>>> 

var="9"*10;
print(var);

my_list = [1, 2, 3] * 3
print(my_list)  # Output: [1, 2, 3, 1, 2, 3, 1, 2, 3]

my_tuple = ("a", "b", "c") * 2
print(my_tuple)  # Output: ('a', 'b', 'c', 'a', 'b', 'c')

