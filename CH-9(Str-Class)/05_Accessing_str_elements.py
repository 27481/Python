""" 
1> s1[index]  only applicable in those containers which have sequencing concept we can use square bracket[] to access elements 
2> print(s1)
3> for loop
4> slicing operator 
"""
u=str('u','t','k','a','r','s','h');
u="hello world"
i=0;
#! accessing str class's element using s1[index]
print(u[3])
print(u[-2])

#! accessing str class's element using for loop
print("Enter some number:\n");
l4=eval(input());
for e in l4:
    print(e);


#! accessing str class's element using while loop
while (i<len(u)):
    print(u[i])
    i+=1;

#! Slicing operator
print();