# dict 

#! dict is a class 
#! dict is mutable 
#! dict is not hashable same as(Set,list)
#! dict is iterable 
#! dict is not a sequence 
#! dict can not have duplicate keys (not data values)
#! indexing is not applicable to dict object 
#! slicing operator is not applicable 
#! dict elements are pair of key-value and data_Value


#* value of key can not be repeated but dataValue can be repeated
