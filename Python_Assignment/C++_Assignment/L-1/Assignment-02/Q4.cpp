// WAP for finding the volume of the cylinder by taking radius and height as input 

#include<iostream>
#include<bits/stdc++.h>

using namespace std;
int main(){
    int r,h;
    cout<<"Enter the radius and height of the cylinder so that you can find the volume respectively:\n";
    cin>>r>>h;
    cout<<3.14*r*h<<endl;

    return 0;
}