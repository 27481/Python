# Single line if else 

#! python does not have conditional operator but it has its substitution known as Single line if else 


""" 
Python does have a conditional operator, but it is a little different from the ternary operator (?:) in C++.

In Python, the conditional operator is expressed using a different syntax using the if and else keywords. Here's an example:
"""
x = 5
y = 10

max_val = x if x > y else y
print(max_val)



